package com.company;

public class SystemOutPrintTips {
    public static void main(String[] args) {
        System.out.println();

        System.out.println("Relationship among the \"System\" class, \"out\" object, and the \"print\" and \"println\" methods");
        System.out.println();
        System.out.println();
        System.out.println("[SYSTEM]");
        System.out.println("[class]");
        System.out.println("  |");
        System.out.println("  |");
        System.out.println("[OUT]");
        System.out.println("[object]");
        System.out.println("  |");
        System.out.println("  |");
        System.out.println("  -----------[PRINT]");
        System.out.println("             [method]");

        System.out.println("  |");
        System.out.println("  |");
        System.out.println("  -----------[PRINTLN]");
        System.out.println("             [method]");
    }
}