package com.company;

public class EscapeSequences {
    public static void main(String[] args){
        System.out.println("Here are some of the common escape sequences featured in Java:");
        System.out.println("Escape Sequence | Name | Description");
        System.out.println("\\n | Newline | Advances the cursor to the next line for subsequent printing");
        System.out.println("\\t | Horizontal tab | Causes the cursor to skip over the next tab stop");
        System.out.println("\\b | Backspace | Causes the cursor to back up, or move left, one position");
        System.out.println("\\r | Return | Causes the cursor to go to the beginning of the current line," +
                "not the next line");
        System.out.println("\\\\ | Backslash | Causes a backslash to be printed");
        System.out.println("\\' | Single quote | Causes a single quotation mark to be printed ");
        System.out.println("?? | Double quote | Causes a double quotation mark to be printed ");
        System.out.println("Man, how do we get that last one!? Double quotes?!\n");

        System.out.println("\"Quotes on quotes on quotes\"");




    }
}
