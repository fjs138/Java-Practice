package com.company;

public class Variable {
    public static void  main(String[] args){

        //here's some variable declaration
        int value;

        //here's an assignment statement
        value = 5;

        System.out.println("The value is ");
        System.out.println(value);
        System.out.println();

        System.out.println("Now, let's be funny\n");
        System.out.println("The value is ");
        System.out.println("value");
        System.out.println("See what I did there!?");
    }
}
