package com.company;

public class StringLength {
    public static void main(String[] args) {
        String firstName = "Frank";
        int stringLength;

        stringLength = firstName.length();

        System.out.println(firstName + " has " + stringLength + " characters.");

        char firstLetter;
        firstLetter = firstName.charAt(0);

        System.out.println("The first letter of " + firstName + " is " + firstLetter);
        System.out.println("Frank in all caps is: " + firstName.toUpperCase());

    }
}

